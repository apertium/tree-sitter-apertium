static inline bool sym_regex_string_character_set_1(int32_t c) {
  return (c < '('
    ? (c < '\r'
      ? (c < '\t'
        ? c == 0
        : c <= '\n')
      : (c <= '\r' || (c < '#'
        ? c == ' '
        : c <= '#')))
    : (c <= '+' || (c < '?'
      ? (c < ':'
        ? c == '/'
        : c <= ':')
      : (c <= '?' || (c >= '[' && c <= ']')))));
}

static inline bool sym_identifier_character_set_1(int32_t c) {
  return (c < ':'
    ? (c < '\r'
      ? (c < '\t'
        ? c == 0
        : c <= '\n')
      : (c <= '\r' || (c < '('
        ? c == ' '
        : c <= '+')))
    : (c <= ':' || (c < '['
      ? (c < '>'
        ? c == '<'
        : c <= '?')
      : (c <= '[' || (c < '|'
        ? c == ']'
        : c <= '|')))));
}

