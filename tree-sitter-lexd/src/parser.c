static inline bool sym_identifier_character_set_1(int32_t lookahead) {
  return
    lookahead == 0 ||
    lookahead == '\t' ||
    lookahead == '\n' ||
    lookahead == '\r' ||
    lookahead == ' ' ||
    ('(' <= lookahead && lookahead <= '+') ||
    lookahead == '<' ||
    lookahead == '>' ||
    lookahead == '?' ||
    lookahead == '[' ||
    lookahead == ']' ||
    lookahead == '|';
}

static inline bool sym_tag_character_set_1(int32_t lookahead) {
  return
    lookahead == 0 ||
    lookahead == '\t' ||
    lookahead == '\n' ||
    lookahead == '\r' ||
    lookahead == ' ' ||
    ('(' <= lookahead && lookahead <= ',') ||
    lookahead == '<' ||
    lookahead == '>' ||
    lookahead == '?' ||
    lookahead == '[' ||
    lookahead == ']' ||
    lookahead == '^' ||
    lookahead == '|';
}

